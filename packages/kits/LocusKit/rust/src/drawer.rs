//! Drawer struct. Ports `Drawer.swift`.
//!
//! A drawer is what callers ingest and what the search pipeline returns.
//! The `content` field is preserved unchanged ŌĆö no truncation, no
//! normalization ŌĆö because the verbatim-first pillar of MemPalace
//! requires that retrieval surface exactly what was filed.
//!
//! Every drawer belongs to a room node in the estate's containment tree
//! (ADR-017). The `parent_node_id` field is the UUID of the room node.
//! Display names (wing, room) are resolved via `DrawerStore::resolve_node_names`
//! when needed ŌĆö they are not stored on the Drawer struct.
//!
//! `embedding_model_id` is present from day one even though no
//! embeddings are generated in this mission. The modelID-tagging rule
//! (one of the architectural constraints of Loci Mode) requires every
//! drawer to carry the identifier of the embedder that produced its
//! vector, so vectors generated by different models can never be
//! compared accidentally.
//!
//! `tombstoned_at` and `removed_by_batch` belong to the soft-delete
//! machinery added in Rev 2.0. They are present on the schema from
//! Rev 1.0 so the layout never needs to migrate.
//!
//! ## Swift-to-Rust shape changes
//!
//! - `Date filedAt` ŌåÆ `i64 filed_at` (epoch seconds). Matches the
//!   leaf-type convention documented in `audit_types.rs` / `manifest.rs`:
//!   the Rust port stores timestamps as i64 epoch seconds and the
//!   persistence-kit `TypedValue::Timestamp(i64)` carries them on the wire.
//!   The Swift port uses `Date` because the storage layer there hands
//!   back `Date`; both legs agree on the canonical wire shape (ISO8601
//!   in SQLite).
//! - `UUID lineageID` ŌåÆ `uuid::Uuid lineage_id`. Same value semantics.

