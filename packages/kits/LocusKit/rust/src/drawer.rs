#[cfg(test)]
mod tests {
    use super::*;

    fn sample() -> Drawer {
        Drawer::new(
            "d1",
            "hello",
            "test-parent",
            "alice",
            1_700_000_000,
            "test-v1",
        )
    }

    #[test]
    fn default_bitmaps_are_zero() {
        let d = sample();
        assert_eq!(d.provenance, 0);
        assert_eq!(d.adjective_bitmap, 0);
        assert_eq!(d.operational_bitmap, 0);
    }

    #[test]
    fn default_udc_code_is_empty_string() {
        // Spec contract: empty string means "no anchor declared". Distinct
        // from None ŌĆö the SQLite column is NOT NULL DEFAULT ''.
        let d = sample();
        assert_eq!(d.udc_code, "");
        assert!(d.udc_facets.is_none());
        assert!(d.wikidata_qid.is_none());
    }

    #[test]
    fn fresh_lineage_per_construction() {
        let a = sample();
        let b = sample();
        assert_ne!(a.lineage_id, b.lineage_id);
    }

    #[test]
    fn source_type_decodes_bits_0_5_cookbook_2_5() {
        let mut d = sample();
        d.provenance = SourceType::Canonical.raw_value();
        assert_eq!(d.source_type(), SourceType::Canonical);
        // High bits don't leak in.
        d.provenance = SourceType::Ambient.raw_value() | (1 << 50);
        assert_eq!(d.source_type(), SourceType::Ambient);
    }

    #[test]
    fn channel_decodes_bits_6_11_cookbook_2_5() {
        let mut d = sample();
        d.provenance = Channel::McpAgent.raw_value() << 6;
        assert_eq!(d.channel(), Channel::McpAgent);
    }

    #[test]
    fn confirmation_decodes_bits_18_23_cookbook_2_5() {
        let mut d = sample();
        d.provenance = Confirmation::UserConfirmed.raw_value() << 18;
        assert_eq!(d.confirmation(), Confirmation::UserConfirmed);
    }

    #[test]
    fn confidence_decodes_bits_24_29_cookbook_2_5() {
        let mut d = sample();
        d.provenance = Confidence::High.raw_value() << 24;
        assert_eq!(d.confidence(), Confidence::High);
    }

    #[test]
    fn sensitivity_decodes_bits_30_35_cookbook_2_5() {
        let mut d = sample();
        d.provenance = Sensitivity::Restricted.raw_value() << 30;
        assert_eq!(d.sensitivity(), Sensitivity::Restricted);
    }

    #[test]
    fn enrichment_status_decodes_bits_36_41_cookbook_2_5() {
        let mut d = sample();
        d.provenance = EnrichmentStatus::QidCompleted.raw_value() << 36;
        assert_eq!(d.enrichment_status(), EnrichmentStatus::QidCompleted);
    }

    #[test]
    fn is_user_confirmed_only_when_userconfirmed_exactly() {
        let mut d = sample();
        d.provenance = Confirmation::UserConfirmed.raw_value() << 18;
        assert!(d.is_user_confirmed());

        // AutomatedConfirmed/PeerConfirmed/ActuatorConfirmed are NOT user-confirmed.
        d.provenance = Confirmation::AutomatedConfirmed.raw_value() << 18;
        assert!(!d.is_user_confirmed());
        d.provenance = Confirmation::PeerConfirmed.raw_value() << 18;
        assert!(!d.is_user_confirmed());
    }

    #[test]
    fn future_version_raw_values_fail_safe() {
        // Reserved raws must decode to safe fallbacks per cookbook ┬¦2.5.
        let mut d = sample();
        d.provenance = 0x3F; // bits 0ŌĆō5 = 63, reserved SourceType
        assert_eq!(d.source_type(), SourceType::User);
        d.provenance = 0x3F << 18; // bits 18ŌĆō23 = 63, reserved Confirmation
        assert_eq!(d.confirmation(), Confirmation::Unconfirmed);
    }
}

#[cfg(test)]
mod sealed_bit_tests {
    use substrate_kernel::bit_field;

    #[test]
    fn bit_27_set_means_sealed() {
        let adj = bit_field::write_flag(true, 0, 27);
        assert!(bit_field::extract_flag(adj, 27));
    }

    #[test]
    fn bit_27_clear_means_unsealed() {
        assert!(!bit_field::extract_flag(0, 27));
    }

    #[test]
    fn sealed_independent_of_trust() {
        let adj = bit_field::write_field(3, 0, 18, 6); // canonical
        assert!(
            !bit_field::extract_flag(adj, 27),
            "trust set must not set seal"
        );
        let sealed = bit_field::write_flag(true, adj, 27);
        assert!(bit_field::extract_flag(sealed, 27));
        assert_eq!((sealed >> 18) & 0x3F, 3, "sealing must not disturb trust");
    }

    #[test]
    fn bit_27_does_not_collide_with_bit_26() {
        let only_seal = bit_field::write_flag(true, 0, 27);
        assert!(
            !bit_field::extract_flag(only_seal, 26),
            "27 must not read as 26"
        );
    }
}
